package Giorno4.GarageDinamico;

public class Auto {
	private String modello;
	private int cavalli;
	
	public Auto(String modello, int cavalli) {
		this.modello = modello;
		this.cavalli = cavalli;
	}
	
	public String getModello() {
		return modello;
	}
	
	public int getCavalli() {
		return cavalli;
	}
	
	public String toString() {
		return toString();
	}

}
